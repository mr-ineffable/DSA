#include<bits/stdc++.h>
using namespace std;
int main(){
    cout << "Bitwise and operator " << 1 <<" & " << 2 << ": " << (1 & 2) << endl;
    cout << "Bitwise or operator " << 1 << "|" << 2 <<" : " <<(1 | 2) << endl;
    cout << "Bitwise xor operator " << 1 << "^" << 2 <<" : " <<(1 ^ 2) << endl;
    cout << "Bitwise >>(Right shift) operator " << (5>>1) << endl;
    cout << "Bitwise <<(Left shift) operator " << (5<<1) << endl;
    //bitwise Not only unary operator
    cout << "Bitwise ~ operator" << ~5 << endl;
}